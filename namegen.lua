    text = "Somehow, a small forest has sprouted up undergound. The fact that all these plants and trees are able to grow without the sun would be fascinating to a botanist, but you're not one, so you don't think much of it."
    end
    return text
end

function namegen:generate_dungeon_name()
  local dnames = {"Dungeon","Keep","Fortress","Castle","Palace","Halls","Dungeons","Tomb","Chamber","Chambers","Hallways","Rooms"}
  local concepts = self.lists.levelConcepts
  local adjectives = self.lists.levelAdjectives
    
  local dtype = random(1,3)
  if (dtype == 1) then
    return "The " .. dnames[random(#dnames)] .. " of " .. concepts[random(#concepts)]
  elseif (dtype == 2) then
    return "The " .. adjectives[random(#adjectives)] .. " " .. dnames[random(#dnames)]
  elseif (dtype == 3) then
    return "The " .. dnames[random(#dnames)] .. " of " .. adjectives[random(#adjectives)] .. " " .. concepts[random(#concepts)]
  end
end

function namegen:generate_dungeon_description()
  local oldUses = {"torture","hold","imprison","impress","house","show off to","intimidate","pay homage to","calm","please","pleasure","upset","irritate","experiment on","study","ignore","employ"}
  local oldSubject = {"enemies","prisoners","friends","subjects","wives","husbands","spouses","allies","inner demons","advisers","siblings","parents","concubines","best friend","pets","conquests","body doubles","children","miners","in-laws","illegitimate children","sons","daughters","mother-in-law","best friend","worst enemy","favorite people"}
  local currUses = {"home to vermin", "used as a trash dump", "converted into apartments", "used as a squat","used for pretty much the same purpose, but by someone else","pretty much forgotten about","a major tourist destination","pointedly ignored","redecorated","renovated","spruced up a bit","left to rot","a hotspot of paranormal activity","generally considered haunted","turned into a historical monument","kind of falling apart","needing some major work, but nobody's done anything","kind of smelly","kind of dirty","impossible to clean","used as a temple to " .. self:generate_god_name()}
  local timeModifier = {"nearly","almost","over","about","sometime around","maybe","more than","around","roughly"}
  local descType = random(1,7)
  if descType == 1 or descType == 2 or descType == 3 then --king made it
    local gender = (random(1,2) == 1 and "male" or "female")
    local rulerFull = self:generate_ruler_name(gender)
    local text = "These chambers were built by " .. rulerFull .. " " .. timeModifier[random(#timeModifier)] .. " " .. random(1,90) .. (random(1,3) == 1 and "" or (random(1,2) == 1 and "0" or "00")) .. " years ago."
    return text .. (random(1,4) == 1 and " The reason " .. (gender == "male" and "he" or "she") .. " built them has been lost to history" or (gender == "male" and " He" or " She") .. " built them to " .. oldUses[random(#oldUses)] .. " " .. (gender == "male" and "his" or "her") .. " " .. oldSubject[random(#oldSubject)]) .. ".\nSince then, they've been " .. currUses[random(#currUses)] .. "."
  elseif descType == 4 or descType == 5 or descType == 6 then --unknown, archeological
    local gender = (random(1,2) == 1 and "male" or "female")
    local ruler = (random(1,4) == false and false or (gender == "male" and self.lists.rulerTypeMale[random(#self.lists.rulerTypeMale)] or self.lists.rulerTypeFemale[random(#self.lists.rulerTypeFemale)]))
    local text = "Nobody knows for sure who built these chambers, but archeologists believe they " .. (not ruler and "were built" or "were built by a forgotten " .. ruler) .. " " .. timeModifier[random(#timeModifier)] .. " " .. random(1,90) .. "00 years ago."
    return text .. (random(1,4) == 1 and " Nobody's really sure why they were built, and nobody really cares" or " It seems as though " .. (not ruler and "they were built to " or (gender == "male" and "he built them to " or "she built them to ")) .. oldUses[random(#oldUses)] .. " " .. (ruler and (gender == "male" and "his " or "her ") or "someone's ") .. oldSubject[random(#oldSubject)]) .. ".\nSince then, they've been " .. currUses[random(#currUses)] .. "."
  elseif descType == 7 then --totally unknown
    return "Someone has carved out a series of rooms and corridors into the rock here. You've not sure why, but you also don't really care why. It looks as though they've been " .. currUses[random(#currUses)] .. "."
  end
end

