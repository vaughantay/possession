function namegen:generate_rat_name(creature)
  local fname = self:generate_first_name(creature)
  if random(1,5) == 1 then
    if string.match(string.sub(fname,-3),"[bcdfgjklmnpstvwyz]a[bcdfgjklmnprstvwyz]") then
      fname = string.sub(fname,1,-4) .. "rat"
    elseif
      string.match(string.sub(fname,-3),"r[aeiou][bcdfgjklmnprstvwyz]") then
      fname = string.sub(fname,1,-4) .. "rat"
    end
    fname = ucfirst(string.gsub(fname,"[BCDFGJKLMNPSTVWYZ]a[bcdfgjklmnprstvwz]","rat"))
    fname = ucfirst(string.gsub(fname,"ra[bcdfgjklmnprstvwyz]","rat"))
    fname = ucfirst(string.gsub(fname,"[bcdfgjklmnpstvwyzBCDFGJKLMNPSTVWYZ]at","rat"))
    if string.sub(fname,-2) == "ra" then
      fname = fname .. "t"
    end
  end
  return fname .. " " .. self.lists.rat[random(#self.lists.rat)]
end

function namegen:generate_dwarf_name(creature, whichOnly)
  local firstnames = {"Urist","Durin","Gimli","Happy","Sneezy","Grumpy","Dopey","Sleepy","Doc","Bashful"}
  local dwarfythings = self.lists.dwarf
  local firstName = ""
  local lastName = ""
  
  whichOnly = whichOnly or (random(1,4) == 1 and (random(1,2) == 1 and "last" or "first") or false)
  
  if whichOnly ~= "last" then
    local firstNameType = random(1,5)
    if firstNameType == 1 and whichOnly ~= "first" then
      firstName = firstnames[random(#firstnames)]
    else
      firstName = self.lists.dwarfFirst[random(#self.lists.dwarfFirst)] .. self.lists.dwarfLast[random(#self.lists.dwarfLast)]
    end
  end
  
  if whichOnly ~= "first" then
    local lastNameType = random(1,3)
    if lastNameType == 1 and creature and whichOnly ~= "last" then
      lastName = self.lists.dwarfFirst[random(#self.lists.dwarfFirst)] .. self.lists.dwarfLast[random(#self.lists.dwarfLast)] .. (creature.gender == "male" and "sson" or "sdottir")
    else
      local lastname = dwarfythings[random(#dwarfythings)]
      local lastname2 = dwarfythings[random(#dwarfythings)]
      while lastname2 == lastname do
        lastname2 = dwarfythings[random(#dwarfythings)]
      end
      lastName = ucfirst(lastname) .. lcfirst(lastname2)
    end
  end
  
  return firstName .. (not whichOnly and " " or "") .. lastName
end

