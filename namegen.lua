function namegen:generate_painting()
  local subjects={"cats","deer","demons","dogs","drunks","farmers","giants","ghosts","horses","hunters","laborers","men","monsters","pigs","soldiers","vampires","wizards","wolves","women"}
  local objects={"a bunch of barrels","farm equipment","a fire","a grave","a pile of skulls","a trebuchet"}
  local surroundings={"on a beach","on a city street","in a field","in a graveyard","at the base of a mountain","on top of a mountain","in the mountains","by a river","on the rim of a volcano","in the woods"}
  local modifier={"It is snowy","It is raining","The scene is painted from a distance","Everything is on fire","There's blood everywhere"}
  local buildings={"barn","castle","cottage","house","hut","longhouse","tower"}
  local rooms={"ballroom","bedroom","cellar","courtyard","dining hall","dungeon","kitchen","laboratory","library","staircase","tavern","tunnel"}
  local thingsInDistance = {"army","lake","forest","graveyard","mountain","mountain range","ocean","stampede","volcano"} -- +buildings
  local adjective={"abstract","amateurish","baroque","colorful","disconcerting","disturbing","grotesque","moody","ornate","otherworldly","pastoral","realistic","sensual","unnerving","violent"}
  local adjectiveMod = {"distractingly","extremely","grotesquely","overly","rather","slightly","somewhat","very"}
  local verb={"dancing","drinking","celebrating","fighting","hunting","sleeping","standing around"}
  local verbWithObject={"dancing with","drinking with","eating","eating with","fighting","hunting","making love to","running from","staring down"}
  
  local ptype = random(1,2)
  local text = "A " .. adjectiveMod[random(#adjectiveMod)] .. " " .. adjective[random(#adjective)]  .. " painting depicting "
  if ptype == 1 then --subjects
    local room = random(0,1)
    local subj2 = random(0,1)
    text = text .. subjects[random(#subjects)] .. " " .. (subj2 == 1 and verbWithObject[random(#verbWithObject)] .. " " .. subjects[random(#subjects)] or verb[random(#verb)]) .. " " .. (room == 1 and "in a " .. rooms[random(#rooms)] or surroundings[random(#surroundings)]) .. " near " .. objects[random(#objects)] .. ". There is a " .. thingsInDistance[random(#thingsInDistance)] .. (room == 1 and " seen through a window." or " in the distance.")
  elseif ptype == 2 then --landscape
    text = text .. "a " .. buildings[random(#buildings)] .. " " .. surroundings[random(#surroundings)] .. ". There is a " .. thingsInDistance[random(#thingsInDistance)] .. " in the distance."
  end
  return text .. " " .. modifier[random(#modifier)] .. "."
end