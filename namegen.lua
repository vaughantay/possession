function namegen:generate_human_name(creature)
  local fname = ""
  local lastname = ""
  local fnames = nil
  local gender = nil

  --First name:
  if random(1,4) ~= 1 then
    if random(1,10) == 1 then
      fname = self:generate_hippie_name()
    else
      if creature == nil or (creature.gender ~= "male" and creature.gender ~= "female") then
        if random(0,1) == 1 then gender = "male" else gender = "female" end
      else
        gender = creature.gender
      end
      if (gender == "male") then
        fnames = self.lists.firstNamesMale
      else
        fnames = self.lists.firstNamesFemale
      end
      fname = fnames[random(#fnames)]
    end
  else --Initial only:
    fname = self.lists.letters[random(#self.lists.letters)] .. "."
  end
  
  --Middle initial?
  if random(1,5) == 1 then
    fname = fname .. " " .. self.lists.letters[random(#self.lists.letters)] .. "."
  end
  
  --Last name
  lastname = self:generate_human_last_name()
  
  if random(1,5) == 1 then
    return self.lists.adjectives[random(#self.lists.adjectives)] .. " " .. fname .. (random(1,3) == 1 and "" or " " .. lastname)
  end
  return fname .. " " .. lastname
end

function namegen:generate_human_last_name()
  local lastname = ""
  if random(1,3) ~= 1 then
    if random(1,10) == 1 then
      lastname = self:generate_spartan_name()
    else
      lastname = self.lists.lastNames[random(#self.lists.lastNames)]
    end
  else --compound name
    if random(1,2) == 1 then
      lastname = self.lists.adjectives[random(#self.lists.adjectives)] .. lcfirst(self.lists.nouns[random(#self.lists.nouns)])
    else
      lastname = self.lists.nouns[random(#self.lists.nouns)] .. lcfirst(self.lists.genericEndings[random(#self.lists.genericEndings)])
    end
  end
  return lastname
end

